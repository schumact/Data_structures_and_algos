// stack using an array
// constant time
var stack = []
stack.push("google");
stack.push("instagram");
stack.push("youtube");
stack.pop();

// O(n)
var other_stack = [];
other_stack.unshift('Create new file');
other_stack.unshift("resized file");
other_stack.unshift("cloned out wrinkle");
other_stack.shift();